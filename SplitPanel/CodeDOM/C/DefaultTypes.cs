using System;

namespace WindowHelper.CodeDOM.C
{
	public static class DefaultTypes
	{
		public static CodeType VoidType        { get; private set; }
		public static CodeType VoidPointerType { get; private set; }

		static DefaultTypes()
		{
			VoidType        = new VoidType();
			VoidPointerType = new PointerType(VoidType);
		}
	}
}