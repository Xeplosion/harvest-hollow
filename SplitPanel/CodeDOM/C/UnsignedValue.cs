using System;
using System.Text;

namespace WindowHelper.CodeDOM.C
{
	public class UnsignedValue : CodeValue
	{
		private ulong value;

		public UnsignedValue(ulong value) : base(value) { }

		public ulong Value
		{
			get { return value; }
			set
			{
				ValueObject = value;
				this.value  = value;
			}
		}
	}
}